x = "This is a sample string"

print("x=", x)

# accessing the first charcter 'T'
print('x[0]=', x[0])

# accessing the second character 'h'
print('x[1]=', x[1])

# accessing from the right side 'g'
print('x[-1]=', x[-1])

# access 0-5 ('This ')
print('x[:6]= ', x[:6])

# access 6 to the end ('is a sample string)
print('x[6:]= ', x[6:])
# access 6 to 8
print('x[6:9]=', x[6:9])
